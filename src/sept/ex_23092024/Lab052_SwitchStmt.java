package ex_23092024;

public class Lab052_SwitchStmt {
    public static void main(String[] args) {
        //Without break
        int day = 1;
        switch (day) {
            case 1:
                System.out.println("Monday");

            case 2:
                System.out.println("tue");
            case 3:
                System.out.println("Wed");
        }
    }
}
