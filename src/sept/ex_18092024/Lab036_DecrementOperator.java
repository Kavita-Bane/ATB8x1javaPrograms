package ex_18092024;

public class Lab036_DecrementOperator {
    public static void main(String[] args) {
        int a=10;
        int result= a--;
        System.out.println(a); // 9
        System.out.println(result); //10

        int b=10;
        int result1= --b;
        System.out.println(b); //9
        System.out.println(result1); //9
    }
}
