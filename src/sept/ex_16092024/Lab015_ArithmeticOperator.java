package ex_16092024;

public class Lab015_ArithmeticOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 2;
        // Binary operators and requires 2 operands to perform operation
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        // % Modulus - Remainders
        System.out.println(a%b);
        // 2 | 10 | 5 - Quotient
        //   |10 |
        //---------------------
        // 0 - Remainder
        System.out.println(67%13);
        // 13 | 65 | 5
        //    | 65 |
        //--------------------
        // 2  Remainder
    }
}
