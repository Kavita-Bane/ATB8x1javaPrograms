package ex_16092024;

public class Lab017_UnaryOperator {
    public static void main(String[] args) {
        //Unary operator (Only one operand )
        int a = 65 ; // By default the value will be +65
        int b = -1 ; //unary minus
        System.out.println(a);
        System.out.println(b);
        b = b+1;
        System.out.println(b);
        int age = 65 ;
        int num = -1 ;
        System.out.println(age + num);


    }
}
